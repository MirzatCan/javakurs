package Gun07;

public class _08_StringEndWith {
    public static void main(String[] args) {

        String s1="Merhaba Dunya";

        System.out.println("s1.endsWith(\"ya\") = " + s1.endsWith("ya"));
        System.out.println("s1.endsWith(\"ba\") = " + s1.endsWith("ba"));
        System.out.println("s1.endsWith(\"A\") = " + s1.endsWith("A"));
        System.out.println("s1.endsWith(\"Dunya\") = " + s1.endsWith("Dunya"));

    }
}
