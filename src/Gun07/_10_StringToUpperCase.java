package Gun07;

public class _10_StringToUpperCase {
    public static void main(String[] args) {
        // toUpperCase : Stringi buyuk harfe donusturur

        String s1="Merhaba Dunya";

        System.out.println("s1 = " + s1);
        System.out.println("s1.toUpperCase() = " + s1.toUpperCase());

        System.out.println("s1.toUpperCase().startsWith(\"ME\") = " + s1.toUpperCase().startsWith("ME"));

    }
}
