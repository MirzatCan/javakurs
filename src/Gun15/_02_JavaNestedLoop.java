package Gun15;

public class _02_JavaNestedLoop {
    public static void main(String[] args) {
        // 2 lerin carpim tablosunu sormustuk.
        //2*1=2
        //2*2=4
        //.....

        //5(dahil)lere kadar halini yapiniz.

        for (int j=1; j<6; j++){
            for (int i=1; i<10;i++){
            System.out.println(j+"*"+i+"="+(j*i));}
        System.out.println();}
    }
}
